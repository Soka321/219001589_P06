


CREATE TABLE MODULE(
M_CODE VARCHAR(10)unique NOT NULL,
M_NAME VARCHAR(255) NOT NULL,
M_CREDIT INT NOT NULL,
M_DURATION INT NOT NULL,
primary key(M_CODE)
);

CREATE TABLE STUDENT(
S_ID VARCHAR(9) UNIQUE NOT NULL,
S_NAME VARCHAR(255) NOT NULL,
S_SURNAME VARCHAR(255) NOT NULL,
S_DOB DATE NOT NULL,
S_GENDER VARCHAR(1) NOT NULL,
primary key(S_ID)

);

CREATE TABLE DEGREE(
D_CODE VARCHAR(6) NOT NULL,
D_NAME VARCHAR(255)NOT NULL,
D_MIN_APS INT NOT NULL,
primary key(D_CODE)
);

CREATE TABLE BRIDGE(
S_ID VARCHAR(9) unique NOT NULL,
M_CODE VARCHAR(10) unique NOT NULL,
Final_Mark DOUBLE NOT NULL,
primary key(S_ID,M_CODE),
FOREIGN KEY(M_CODE) REFERENCES MODULE(M_CODE),
FOREIGN KEY(S_ID) REFERENCES STUDENT(S_ID)
);


CREATE TABLE STUdent_Degree_BRIDGE(
S_ID CHAR(9) unique NOT NULL,
D_CODE VARCHAR(6) unique NOT NULL,

primary key(S_ID,D_CODE),
FOREIGN KEY(D_CODE) REFERENCES DEGREE(D_CODE),
FOREIGN KEY(S_ID) REFERENCES STUDENT(S_ID)
);



